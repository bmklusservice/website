const translatableSelector = [
  "a",
  "button",
  "p",
  "h1",
  "h2",
  "h3",
  "span",
  "small",
  "li",
  "dt",
  "dd"
].join(",");

function normalizeText(value) {
  return value.replace(/\s+/g, " ").trim();
}

function translateElement(element, language) {
  if (element.closest("script, style")) {
    return;
  }

  if (element.children.length > 0) {
    return;
  }

  if (!element.dataset.enText) {
    element.dataset.enText = normalizeText(element.textContent);
  }

  const english = element.dataset.enText;
  if (!english || !translations[english]) {
    return;
  }

  element.textContent = language === "nl" ? translations[english] : english;
}

function translateFormLabels(language) {
  document.querySelectorAll("label").forEach((label) => {
    const textNode = Array.from(label.childNodes).find(
      (node) => node.nodeType === Node.TEXT_NODE && normalizeText(node.textContent)
    );

    if (!textNode) {
      return;
    }

    if (!label.dataset.enLabel) {
      label.dataset.enLabel = normalizeText(textNode.textContent);
    }

    const english = label.dataset.enLabel;
    if (translations[english]) {
      textNode.textContent = `${language === "nl" ? translations[english] : english}\n              `;
    }
  });
}

function applyLanguage(language) {
  document.documentElement.lang = language;
  localStorage.setItem("site-language", language);

  document.querySelectorAll(translatableSelector).forEach((element) => {
    if (!element.classList.contains("language-toggle")) {
      translateElement(element, language);
    }
  });

  translateFormLabels(language);

  document.querySelectorAll(".language-toggle button").forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function setupLanguageToggle() {
  const header = document.querySelector(".site-header");
  if (!header) {
    return;
  }

  const toggle = document.createElement("div");
  toggle.className = "language-toggle";
  toggle.setAttribute("aria-label", "Language");
  toggle.innerHTML = `
    <button type="button" data-language="en">EN</button>
    <button type="button" data-language="nl">NL</button>
  `;
  header.append(toggle);

  const requestedLanguage = new URLSearchParams(window.location.search).get("lang");
  const savedLanguage =
    requestedLanguage === "nl" || requestedLanguage === "en"
      ? requestedLanguage
      : localStorage.getItem("site-language") === "nl"
        ? "nl"
        : "en";
  applyLanguage(savedLanguage);

  toggle.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.language);
    });
  });
}

const lightbox = document.querySelector(".lightbox");
const lightboxImage = lightbox?.querySelector("img");
const closeButton = lightbox?.querySelector("button");

function openLightbox(image) {
  lightboxImage.src = image.src;
  lightboxImage.alt = image.alt;
  lightbox.hidden = false;
  closeButton.focus();
}

function closeLightbox() {
  lightbox.hidden = true;
  lightboxImage.removeAttribute("src");
  lightboxImage.alt = "";
}

document.querySelectorAll("[data-lightbox]").forEach((image) => {
  image.addEventListener("click", () => openLightbox(image));
});

if (lightbox && closeButton && lightboxImage) {
  closeButton.addEventListener("click", closeLightbox);

  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
      closeLightbox();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !lightbox.hidden) {
      closeLightbox();
    }
  });
}

setupLanguageToggle();
